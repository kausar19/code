#max in A to Z
max_char = max('abcdefghijklmnopqrstuvwxyz')
print(max_char)

#max in 0 to !000
max_num = max(range(1001))
print(max_num)

#max in a string
print(max("abcd123"))

#max in a list
max_list = max(['h', 'e', 'l', 'l', 'o', 'w', 'o', 'r', 'l', 'd'])
print(max_list)

#max with key parameter
max_key = max(['science', 'biology', 'chemistry', 'english'])
print(max_key)

#max of of a list of tuples
max_tuple = max([('x','y'), ('a', 'b'), ('c', 'd')])
print(max_tuple)